#include <unistd.h>

void	ft_print_comb(void)
{
	int		num;
	char	arr[3];

	num = 0;
	while (num <= 789)
	{
		arr[0] = '0' + num / 100;
		arr[1] = '0' + (num / 10) % 10;
		arr[2] = '0' + num % 10;
		if ((num / 100) < ((num / 10) % 10) && ((num / 10) % 10) < (num % 10))
		{
			write(1, &arr, 3);
			if (num < 789)
				write(1, ", ", 2);
		}
		num++;
	}
}
